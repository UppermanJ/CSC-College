
#include<iostream>

using namespace std;

int main(int argc, char** argv)
{
   int a = 5;
   cout << endl << endl;
   cout << "------ THE RELATIONAL OPERATORS ------" << endl;
   cout << endl;
   cout << " Relational operators compare two values" << endl;
   cout << " If the comparison is true, the result is 1 otherwise is 0" << endl;
   cout << "                              a = " << a << endl;
   cout << " Less than operator           a <  10: " << ( a <  10 ) << endl;
   cout << " Less or equal to operator    a <= 10: " << ( a <= 10 ) << endl;
   cout << " Greater than operator        a >  10: " << ( a >  10 ) << endl;
   cout << " Greater or equal to operator a >= 10: " << ( a >= 10 ) << endl;
   cout << " Equal to operator            a == 10: " << ( a == 10 ) << endl;
   cout << " Not equal to operator        a != 10: " << ( a != 10 ) << endl;
   cout << endl << endl;
   return 0;
} // End of function main()
